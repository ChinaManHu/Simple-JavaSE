public class UseOfPlusSign {
    public static void main(String[] args) {
        //字符串类型数据和整型数据的相加实验
        //字符串+整型
        System.out.println("100"+1);
        //整型+字符串
        System.out.println(1+"100");
        //字符串+整型+整型
        System.out.println("100"+2+1);
        //整型+整型+字符串
        System.out.println(1+2+"100");
    }
}
